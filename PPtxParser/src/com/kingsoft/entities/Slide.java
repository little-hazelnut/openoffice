package com.kingsoft.entities;

public class Slide {
	private short relationId;
	private long id;
	private SpTree spTree;

	public short getRelationId() {
		return relationId;
	}

	public void setRelationId(short relationId) {
		this.relationId = relationId;
	}

	public long getId() {
		return id;
	}

	public void setId(long id) {
		this.id = id;
	}

	public SpTree getSpTree() {
		return spTree;
	}

	public void setSpTree(SpTree spTree) {
		this.spTree = spTree;
	}
	
	
}
